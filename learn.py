age = 10

if age < 2:
    print("Just a baby")
elif age == 2 and age < 4:
    print("Just a toddler")
elif age == 4 and age < 13:
    print("Just a kid")
elif age == 13 and age < 20:
    print("Just a teenager")
elif age == 20 and age < 65:
    print("Just an adult")
else:
    print("Just an Elder")
